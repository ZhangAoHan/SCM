/****************************** end *********************************/


/********************* 蜂鸣器所在GPIO口宏定义 ***************************/
#define BEEP_GPIO  GPIOB

#define BEEP_PIN   GPIO_Pin_8

#define BEEP PBout(8)     // PB8
/****************************** end *********************************/


/************** 蜂鸣器操作函数宏定义(高电平打开) *********************/
#define BEEP_ON()      BEEP=1    //打开蜂鸣器
#define BEEP_OFF()     BEEP=0    //关闭蜂鸣器
#define BEEP_TOGGLE()  BEEP^=1   //翻转蜂鸣器
/****************************** end *********************************/


