#define TARGET_GLOBAL 1
#include "target.h"
/********************************************
*函数名称：key_init(void)
*函数功能：初始化按键。
*函数入口：无
*函数出口：无
*说明：由于本实例按键接在两个外部中断引脚上
故，本初始化程序实际是对外部中断的初始化
********************************************/
void key_init(void)
{
  EX1 = 1;   //外部中断1使能
  EX0 = 1;	 //外部中断0使能
  IT1 = 1;	 //init1跳变中断
  IT0 = 1;	 //init0跳变中断
}